# python的特色是用缩进表示代码块，不需要使用大括号{}
# 缩进空格数可变， 但是同一个代码块的语句必须包含相同空格数
if True:
    print("True")
else:
    print("False")
# 使用反斜杠表示一条语句，通常一行写完一条语句
total = item_one + \
        item_two + \
        item_three
# 在[]{}()中的语句不需要使用反斜杠\
total = ['item_one', 'item_two', 'item_three']
